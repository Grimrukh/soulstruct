    maps = []
    variables = []
    for m in maps:
        xx = int(m[4:6])
        zz = int(m[7:9])
        size = int(m[10:12])  # 0, 1, 2

        if size >= 10:
            is_alternate = True
            tile_size = size - 10
        else:
            is_alternate = False
            tile_size = size

        if tile_size == 2:
            # Large tile.
            big_xx = xx
            big_zz = zz
            suffix = []
            size_name = "LARGE"
            parent_tile = ""
        elif tile_size == 1:
            # Medium tile.
            big_xx = xx // 2
            big_zz = zz // 2
            medium_suffix = "S" if zz % 2 == 0 else "N"
            medium_suffix += "W" if xx % 2 == 0 else "E"
            suffix = [medium_suffix]
            size_name = "MEDIUM"
            if is_alternate:
                # Large alt should always exist.
                parent_tile = f"LANDOFSHADOW_{big_xx:02d}_{big_zz:02d}_ALT"
            else:
                parent_tile = f"LANDOFSHADOW_{big_xx:02d}_{big_zz:02d}"
        elif tile_size == 0:
            # Small tile.
            medium_xx = xx // 2
            medium_zz = zz // 2
            small_suffix = "S" if zz % 2 == 0 else "N"
            small_suffix += "W" if xx % 2 == 0 else "E"
            medium_suffix = "S" if medium_zz % 2 == 0 else "N"
            medium_suffix += "W" if medium_xx % 2 == 0 else "E"
            big_xx = medium_xx // 2
            big_zz = medium_zz // 2
            suffix = [medium_suffix, small_suffix]
            size_name = "SMALL"
            if is_alternate:
                # Medium alt should always exist.
                parent_tile = f"LANDOFSHADOW_{big_xx:02d}_{big_zz:02d}_{suffix[0]}_ALT"
            else:
                parent_tile = f"LANDOFSHADOW_{big_xx:02d}_{big_zz:02d}_{suffix[0]}"
        else:
            raise ValueError(f"Invalid size: {tile_size}")

        name = f"LandOfShadow_{big_xx:02d}_{big_zz:02d}"
        for s in suffix:
            name += f"_{s}"
        if is_alternate:
            name += "_ALT"
        variable_name = f"LANDOFSHADOW_{big_xx:02d}_{big_zz:02d}"
        for s in suffix:
            variable_name += f"_{s}"
        if is_alternate:
            variable_name += "_ALT"
        verbose_name = f"Land of Shadow {big_xx:02d}-{big_zz:02d}"
        if is_alternate:
            verbose_name += " (Alternate)"
        for s in suffix:
            verbose_name += f" {s}"

        string = [
            f"{variable_name} = MapTile(  # {size_name}",
            f"    {xx}, {zz}, {size},",
            f"    name=\"{name}\",",
            f"    variable_name=\"{variable_name}\",",
            f"    verbose_name=\"{verbose_name}\",",
        ]
        if parent_tile:
            string.append(f"    parent_tile={parent_tile},")
        if is_alternate:
            string.append("    is_alternate=True,")
        string.append(")")
        print("\n".join(string))

        variables.append(variable_name)

    for v in variables:
        print(f"    \"{v}\",")
